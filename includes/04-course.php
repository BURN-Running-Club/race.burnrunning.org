			<p><strong>Half Marathon</strong> - Two laps around Coyote Hills via Bayview Trail, each with an out-and-back addition via Apay Way to the 84 bridge overpass. 2nd lap turns off Bayview Trail on Quail Trail, with an additional loop around Meadowlark Trail at the end (with considerable elevation).</p>
			<p><strong>Course</strong> - The trails are mixed surfaces, partially paved with some gravel and dirt trails, trail shoes are recommended. We will be sharing the park with local residents, horses, and dogs. Runners are advised to run in single file, keep to the right and share the road with cyclist and other users of the park. Course turn markers are color coded for 5K, Half Marathon 1st lap, and Half Marathon 2nd lap respectively. (Images to be added).</p>
			<p><strong>Aid Stations</strong> - There are three aid stations along the course, 5K runners will find them at 1.2mi and 2.9mi, for Half Marathon runners, these two plus 4.2mi, 5.6mi, 7.0mi, 8.6mi, 10.0mi and 11.5mi. If you may require extra water in between, please bring your own water bottle.</p>
